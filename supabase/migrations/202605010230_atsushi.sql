--   (1) MODAL TRIGGER: a `choose_one` trigger effect parks a choose_mode decision
--       (apply_trigger_effects); submit_decision's new trigger_modal branch applies
--       the chosen mode's untargeted actions and resumes. Modal SPELLS are
--       unaffected (they resolve via resolve_top_of_stack, not trigger_modal).
