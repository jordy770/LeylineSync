-- Typed keyword grants — "Zombies you control have flying" (Eternal Skylord),
-- "the Army has deathtouch" (Vizier of the Scorpion), "Other creatures you control
-- have intimidate" (Vela), etc. Authored as a keyword continuous effect with a
-- SCOPE + type filter, exactly like the pump anthem:
--   { "type": "flying", "affected": "controller", "payload": { "creature_type": "Zombie" } }
--
-- register_card_continuous_effects already stores affected:'controller'/'all' +
-- payload.creature_type for keyword effects. The only gap is the READ: each
-- card_has_<keyword> accessor's `affected_card_id is null` (mass) branch granted
-- the keyword to EVERY creature, ignoring the player/type filter. This migration
-- reproduces the 9 combat-keyword accessors with the mass branch now filtered by
-- affected_player_id (the affected card's controller) and payload.creature_type
-- (its type_line) — mirroring the typed-lord pump fold. Backward-compatible:
-- affected:'all' (player null, no creature_type) still grants to everyone.

create or replace function public.card_has_flying(p_session_id uuid, p_game_card_id uuid)
returns boolean language sql security definer set search_path = public as $$
  select exists (
    select 1
    from public.game_continuous_effects effects
    left join public.game_cards source_card on source_card.id = effects.source_card_id
    left join public.game_cards tc on tc.id = p_game_card_id
    left join public.cards tcard on tcard.id = tc.card_id
    where effects.session_id = p_session_id
      and effects.effect_type = 'flying'
      and public.is_session_player(p_session_id, auth.uid())
      and (
        effects.affected_card_id = p_game_card_id
        or (
          effects.affected_card_id is null
          and (effects.affected_player_id is null
               or effects.affected_player_id = coalesce(tc.controller_player_id, tc.owner_id))
          and (effects.payload ->> 'creature_type' is null
               or tcard.type_line ilike '%' || (effects.payload ->> 'creature_type') || '%')
        )
      )
      and (effects.source_zone_required is null or source_card.zone = effects.source_zone_required)
  );
$$;

